    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
   // validasi tabel
    if (tblPembelian.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "Tidak ada data untuk disimpan");
        return;
    }

    // validasi tanggal
    if (jDateChooser1.getDate() == null) {
        JOptionPane.showMessageDialog(this, "Tanggal pembelian wajib diisi");
        return;
    }

    // validasi total
    if (txtTotal.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Total belum dihitung");
        return;
    }

    BigDecimal total = new BigDecimal(txtTotal.getText());

    // ambil tanggal manual
    java.util.Date tgl = jDateChooser1.getDate();
    java.sql.Date sqlDate = new java.sql.Date(tgl.getTime());

    // 1. insert pembelian (HEADER)
    pembelianServ pembServ = new pembelianServ();
    if (!pembServ.insertPembelian(idPembelianGlobal, sqlDate, total)) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan pembelian");
        return;
    }

    // 2. ambil detail dari JTable
    ArrayList<detail_pembelian> list = new ArrayList<>();
    DefaultTableModel model = (DefaultTableModel) tblPembelian.getModel();

    for (int i = 0; i < model.getRowCount(); i++) {
        detail_pembelian dp = new detail_pembelian();

        dp.setId_detail(model.getValueAt(i, 1).toString());
        dp.setId_pembelian(idPembelianGlobal);
        dp.setNama_item(model.getValueAt(i, 3).toString());
        dp.setJumlah(Integer.parseInt(model.getValueAt(i, 4).toString()));
        dp.setHarga(new BigDecimal(model.getValueAt(i, 5).toString()));

        // kode produk (boleh null untuk operasional)
        Object valKode = model.getValueAt(i, 6);
        String kodeProduk = (valKode == null || valKode.toString().trim().isEmpty())
                ? null
                : valKode.toString();
        dp.setKode_produk(kodeProduk); // ðŸ”´ INI TADI BELUM ADA

        // jenis pengeluaran (ENUM)
        dp.setJenis_pengeluaran(
                model.getValueAt(i, 7).toString().trim().toUpperCase()
        );

        // keterangan (boleh kosong)
        Object ket = model.getValueAt(i, 8);
        dp.setKeterangan(ket == null ? null : ket.toString());

        list.add(dp);
    }

    // 3. insert detail (BATCH)
    if (serv.insertBatch(list)) {
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan");

        model.setRowCount(0);
        txtTotal.setText("");
        jDateChooser1.setDate(null);
        kondisiAwal(); // generate idPembelianGlobal baru
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

