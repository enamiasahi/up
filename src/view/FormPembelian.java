    public DefaultTableModel produkModel() {
         String[] judul = {"Kode Barang", "Nama Barang"};

    ArrayList<produk> list = aserv.getListProduk();   // ganti var

    ArrayList<Object[]> barangOnly = new ArrayList<>();

    for (produk p : list) {                           // ganti var
        if (p.getKodeProduk().startsWith("B")) {
            barangOnly.add(new Object[]{
                p.getKodeProduk(),
                p.getNamaProduk()
            });
        }
    }

    Object[][] data = barangOnly.toArray(new Object[barangOnly.size()][]);

    return new DefaultTableModel(data, judul);
    }
    
     private void kondisiAwal() {
         
         idPembelianGlobal = serv.generateIdPembelian(); 
        txtNamaKeperluan.setEditable(false);
        txtSubtotal.setEditable(false);
        BtnLookup.setEnabled(false);

        rbBarangDagang.addActionListener(e -> aturMode());
        rbOperasional.addActionListener(e -> aturMode());

        txtJumlah.getDocument().addDocumentListener(new kalkulasiSubtotal());
        txtHarga.getDocument().addDocumentListener(new kalkulasiSubtotal());
    }

    private void aturMode() {
        if (rbBarangDagang.isSelected()) {
            BtnLookup.setEnabled(true);
            txtNamaKeperluan.setEditable(false);
            txtNamaKeperluan.setText("");
        } else if (rbOperasional.isSelected()) {
            BtnLookup.setEnabled(false);
            txtNamaKeperluan.setEditable(true);
            txtNamaKeperluan.setText("");
        }
    }
    
    private class kalkulasiSubtotal implements javax.swing.event.DocumentListener {

        @Override
        public void insertUpdate(javax.swing.event.DocumentEvent e) { hitung(); }
        @Override
        public void removeUpdate(javax.swing.event.DocumentEvent e) { hitung(); }
        @Override
        public void changedUpdate(javax.swing.event.DocumentEvent e) { hitung(); }

        private void hitung() {
            try {
                int jml = Integer.parseInt(txtJumlah.getText());
                int hrg = Integer.parseInt(txtHarga.getText());
                txtSubtotal.setText(String.valueOf(jml * hrg));
            } catch (Exception ex) {
                txtSubtotal.setText("0");
            }
        }
    }
    
  



   private void hitungTotal() {
    DefaultTableModel model = (DefaultTableModel) tblPembelian.getModel();
    BigDecimal total = BigDecimal.ZERO;

    for (int i = 0; i < model.getRowCount(); i++) {
        Object val = model.getValueAt(i, 5); // kolom subtotal

        if (val instanceof BigDecimal) {
            total = total.add((BigDecimal) val);
        } else if (val != null) {
            // kalau disimpan sebagai string
            total = total.add(new BigDecimal(val.toString()));
        }
    }

    txtTotal.setText(total.toString());
}
   
  
   
