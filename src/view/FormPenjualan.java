    private void btnTambah2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambah2ActionPerformed
 
    // ===== VALIDASI =====
    if (cbProduk.getSelectedIndex() == 0) {
        JOptionPane.showMessageDialog(this, "Pilih produk!");
        return;
    }

    if (txtJumlah.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Jumlah belum diisi!");
        return;
    }

    if (txtHarga.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Harga belum diisi!");
        return;
    }

    if (jDateChooser1.getDate() == null) {
        JOptionPane.showMessageDialog(this, "Tanggal penjualan wajib diisi!");
        return;
    }

    // ===== ID PENJUALAN (dibuat sekali) =====
    if (idPenjualanGlobal == null || idPenjualanGlobal.isEmpty()) {
        idPenjualanGlobal = dServ.generateIdPenjualan();
    }

    // ===== AMBIL PRODUK =====
    String selected = cbProduk.getSelectedItem().toString();
    String kode = selected.split(" - ")[0];
    String nama = selected.split(" - ")[1];

    int jumlah;
    BigDecimal harga;

    try {
        jumlah = Integer.parseInt(txtJumlah.getText());
        harga = new BigDecimal(txtHarga.getText());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Jumlah atau harga tidak valid!");
        return;
    }

    BigDecimal subtotal = harga.multiply(BigDecimal.valueOf(jumlah));

    // ===== TANGGAL =====
    java.util.Date tgl = jDateChooser1.getDate();
    java.sql.Date sqlDate = new java.sql.Date(tgl.getTime());

    // =================================================
    // MODE TAMBAH / EDIT (SAMA PERSIS KAYAK PEMBELIAN)
    // =================================================
    if (rowYangDipilih == -1) {

        // ===== MODE TAMBAH =====
      if (counterDetail == -1) {
    String lastId = dServ.generateIdDetail(); // contoh: DJ0001
    counterDetail = Integer.parseInt(lastId.substring(2)) - 1;
}

counterDetail++;
String idDetail = String.format("DJ%04d", counterDetail);


        modelDetail.addRow(new Object[]{
            sqlDate,
            idDetail,
            idPenjualanGlobal,
            kode,
            nama,
            harga,
            jumlah,
            subtotal
        });

    } else {

        // ===== MODE EDIT =====
        modelDetail.setValueAt(sqlDate, rowYangDipilih, 0);
        // ‚ùå idDetail TIDAK DIUBAH
        modelDetail.setValueAt(kode, rowYangDipilih, 3);
        modelDetail.setValueAt(nama, rowYangDipilih, 4);
        modelDetail.setValueAt(harga, rowYangDipilih, 5);
        modelDetail.setValueAt(jumlah, rowYangDipilih, 6);
        modelDetail.setValueAt(subtotal, rowYangDipilih, 7);

        // balik ke mode tambah
        rowYangDipilih = -1;
    }

    hitungTotal();
    resetInput();
    }//GEN-LAST:event_btnTambah2ActionPerformed

    private void btnTambah1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambah1ActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblPenjualan.getModel();
    int row = tblPenjualan.getSelectedRow();

    // 1. pastikan ada baris dipilih
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus!");
        return;
    }

    // 2. konfirmasi
    int konfirmasi = JOptionPane.showConfirmDialog(
        this,
        "Yakin ingin menghapus item ini?",
        "Konfirmasi",
        JOptionPane.YES_NO_OPTION
    );

    if (konfirmasi != JOptionPane.YES_OPTION) {
        return;
    }

    // 3. hapus baris
    model.removeRow(row);

    // 4. reset pilihan
    rowYangDipilih = -1;

    cbProduk.setSelectedIndex(0);
    txtHarga.setText("");
    txtJumlah.setText("");
    txtSubtotal.setText("");
    
    }//GEN-LAST:event_btnTambah1ActionPerformed

    private void tblPenjualanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPenjualanMouseClicked
    rowYangDipilih = tblPenjualan.getSelectedRow();
    if (rowYangDipilih == -1) return;

    txtHarga.setText(modelDetail.getValueAt(rowYangDipilih, 5).toString());
    txtJumlah.setText(modelDetail.getValueAt(rowYangDipilih, 6).toString());
    txtSubtotal.setText(modelDetail.getValueAt(rowYangDipilih, 7).toString());

    String kode = modelDetail.getValueAt(rowYangDipilih, 3).toString();
    for (int i = 1; i < cbProduk.getItemCount(); i++) {
        if (cbProduk.getItemAt(i).toString().startsWith(kode)) {
            cbProduk.setSelectedIndex(i);
            break;
        }
    }
    }//GEN-LAST:event_tblPenjualanMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
if (tblPenjualan.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "Tidak ada data untuk disimpan");
        return;
    }

    if (jDateChooser1.getDate() == null) {
        JOptionPane.showMessageDialog(this, "Tanggal penjualan wajib diisi");
        return;
    }

    if (txtTotal.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Total belum dihitung");
        return;
    }

    BigDecimal total = new BigDecimal(txtTotal.getText());

    // ubah Date -> String (yyyy-MM-dd)
    java.util.Date tgl = jDateChooser1.getDate();
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    String tanggal = sdf.format(tgl);

    // 1. insert HEADER penjualan
    penjualanServ pnjServ = new penjualanServ();
    if (!pnjServ.insert(idPenjualanGlobal, tanggal, total)) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan penjualan");
        return;
    }

    // 2. ambil DETAIL dari JTable
    ArrayList<detail_penjualan> list = new ArrayList<>();
    DefaultTableModel model = (DefaultTableModel) tblPenjualan.getModel();

    for (int i = 0; i < model.getRowCount(); i++) {
        detail_penjualan dp = new detail_penjualan();

        dp.setIdDetail(model.getValueAt(i, 1).toString());
        dp.setIdPenjualan(idPenjualanGlobal);
        dp.setKodeProduk(model.getValueAt(i, 3).toString());
        dp.setJumlah(Integer.parseInt(model.getValueAt(i, 6).toString()));
        dp.setHarga(new BigDecimal(model.getValueAt(i, 5).toString()));

        list.add(dp);
    }

    // 3. insert detail (batch)
    if (dServ.insertBatch(list)) {
        JOptionPane.showMessageDialog(this, "Penjualan berhasil disimpan");

        model.setRowCount(0);
        txtTotal.setText("");
        jDateChooser1.setDate(null);
        idPenjualanGlobal = null;
    }
    }//GEN-LAST:event_jButton3ActionPerformed


    private void loadProduk() {
    listProduk = pServ.getListProduk();

    cbProduk.removeAllItems();

    // item dummy
    cbProduk.addItem("-- Pilih Produk --");

    for (produk p : listProduk) {
        cbProduk.addItem(p.getKodeProduk() + " - " + p.getNamaProduk());
    }

    cbProduk.setSelectedIndex(0);
}




    private void hitungSubtotal() {
    try {
        BigDecimal harga = new BigDecimal(txtHarga.getText());
        int jumlah = Integer.parseInt(txtJumlah.getText());

        BigDecimal subtotal = harga.multiply(BigDecimal.valueOf(jumlah));
        txtSubtotal.setText(subtotal.toString());
    } catch (Exception e) {
        txtSubtotal.setText("0");
    }
}
    
    private void hitungTotal() {
    BigDecimal total = BigDecimal.ZERO;

    for (int i = 0; i < modelDetail.getRowCount(); i++) {
        BigDecimal sub = new BigDecimal(
            modelDetail.getValueAt(i, 7).toString()
        );
        total = total.add(sub);
    }

    txtTotal.setText(total.toString());
}



   
  
   
